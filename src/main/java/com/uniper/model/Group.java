package com.uniper.model;

public class Group {
	private int id;
	private String groupName;
	
	public int getId(){
		return id;
	}
	public void setId(int Id){
		this.id = Id;
	}
	
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}

}
